## Что такое бакелит, катален и люцит — три ключевых пластика винтажа

### Бакелит (с 1907)

**Бакелит** — это первая в мире синтетическая пластмасса. Его изобрёл бельгийско-американский химик **Лео Бекеланд в 1907 году**. Это термореактивная фенолформальдегидная смола: после полимеризации её невозможно расплавить заново.

В украшения бакелит пришёл в 1920-х. Расцвет — 1930-е, когда формальдегидные браслеты, броши, кольца и заколки стали символом эпохи Art Deco и голливудского гламура. Носили его и Нэнси Кунард — наследница пароходной империи Cunard Line — со своими знаменитыми «варварскими» браслетами.

### Катален (с 1928)

**Катален** (Catalin) — это разновидность бакелита, появившаяся в конце 1920-х. Первоначально её называли «крикливый брат бакелита»: катален выпускался в ярких цветах и продавался в дешёвых магазинах «пять-десять центов» в США. Сегодня резной катален 1930-1940-х — одна из самых востребованных категорий у коллекционеров.

### Люцит (с 1931)

**Люцит** — это акриловый прозрачный пластик, разработанный компанией **DuPont в 1931 году**. Изначально он использовался в военной промышленности (кабины самолётов в Первую мировую и Вторую мировую), а после войны превратился в звезду модных аксессуаров.

В 1950-х в Нью-Йорке и Флориде производили самые знаменитые люцитовые сумки — Llewellyn Inc., Dorset Rex и Wilardy. Прозрачные коробочки с кружевом, ракушками и блёстками внутри стали символом эпохи.

## Четыре теста, чтобы отличить настоящий бакелит

Эти методы используют Наоми Томпсон и Лиз Тригенза — авторы книги *«Style Me Vintage: Accessories»*. Применяйте с осторожностью.

### Тест 1. Запах при трении

Самый простой и эффективный способ. Активно потрите участок предмета пальцем 5-10 секунд. Настоящий бакелит выделит характерный **запах формальдегида** — слегка химический, напоминающий лак для волос или нафталин.

> 💡 **Этикет коллекционера:** на блошином рынке или в антикварном магазине не трите предмет без разрешения продавца. Это считается грубым. Спросите перед тестом.

### Тест 2. Горячая вода

Более надёжный, но рискованный тест. Подержите предмет под горячей водой 30-60 секунд. Если это бакелит, запах формальдегида появится ещё ярче. Не подходит для предметов с металлическими вставками, рисунками или клееными деталями.

### Тест 3. Полироль Simichrome

Розовая полироль для металлов **Simichrome** при нанесении на настоящий бакелит **желтеет**. Это надёжный лабораторный тест, но Simichrome может слегка повреждать поверхность — используйте ватную палочку и наносите на скрытое место (внутренняя сторона браслета).

### Тест 4. Звук при ударе

Возьмите два бакелитовых браслета и аккуратно стукните их друг о друга. Настоящий бакелит даёт **глухой характерный «щёлк»** — не звон стекла и не пластиковый дребезг. Этот звук сложно описать словами, но узнаваем после нескольких сравнений.

## Признаки, по которым легко отличить fakelite

- ❌ **Белый бакелит не существует.** Если вам предлагают «белый бакелит» 1940-х — это подделка. Бакелит со временем темнеет: молочно-белый цвет становится сливочным/янтарным.
- ❌ **Видимые швы от формы.** Настоящий бакелит вырезался из заготовок, а не отливался — швов нет. Швы — признак твёрдого пластика или каталинских отливок второй половины XX века.
- ❌ **Идеально гладкая, «полированная» фактура.** Бакелит имеет лёгкое матовое сияние, не зеркальный блеск.
- ❌ **Уксусный или кислый запах.** Это сигнал — пластик деградирует («the disease»). Не покупайте такие предметы, а уже купленные — изолируйте: они могут «заразить» другие пластиковые вещи.

