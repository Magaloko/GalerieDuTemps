### Сколько стоит винтажная бакелитовая брошь?

В 2026 году обычная нерезная брошь — $50-150. Резные figural-броши (в виде фруктов, ягод, животных, домино) — $150-500. Редкие подписанные пьесы 1930-х (Bakelite Society catalogue-quality) — до нескольких тысяч долларов.

### Что такое «фалесцент» (fakelite)?

Это сленговое название современных пластиковых имитаций бакелита. Чаще всего — недорогая бижутерия из акрила или ABS-пластика, продаваемая в 1990-2010-х под видом винтажа. Не имеет коллекционной ценности.

### Безопасно ли носить бакелит?

Да, после полимеризации бакелит химически инертен — он не выделяет формальдегид при нормальном использовании. Запах при трении — это микроскопическое количество, не опасное для здоровья. Аллергическая реакция возможна только при индивидуальной непереносимости фенолформальдегидных соединений.

