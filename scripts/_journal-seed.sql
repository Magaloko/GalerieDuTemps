## 1980-е: гигантизм, кресты и «фальшивка как идеология»

Десятилетие максимализма. **Если оно не было «слишком», оно не было 80-х.** Гигантские броши, многоэтажные серьги, цепи как у рэпперов.

**Coco Chanel ещё в 1920-х говорила, что лучшие драгоценности — фальшивые.** В 1980-х эту идею взяли в гипертрофированной форме:
- **Chanel под Karl Lagerfeld** запускает массивные серьги-логотипы CC, броши-камелии, многослойные жемчужные колье
