## Как носить винтажные перчатки в 2026

Этикет 1950-х уже не работает буквально, но базовые принципы остаются:

### Правила, которые работают сегодня:

1. **Длина перчатки обратно пропорциональна длине рукава.** Короткое платье без рукавов → длинные перчатки. Платье с длинным рукавом → короткие или вообще без перчаток.

2. **Браслеты можно носить поверх перчаток. Кольца — нельзя.** Это правило 1950-х актуально и сегодня — кольца с пастой над перчаткой смотрятся неуклюже, браслеты-каффы дополняют образ.

3. **Снимая перчатки, делайте это аккуратно.** Тяните по одному пальцу, чтобы не растянуть форму.

4. **Перчатки можно носить летом.** Тонкие хлопковые или вязанные крючком перчатки в жаркую погоду — как чашка чая: парадоксально освежают.

### Образы 2026 года с винтажными перчатками:

- **Jackie O look**: простое чёрное платье + длинные кожаные перчатки + большие солнцезащитные очки
- **Wedding guest**: пастельное платье миди + кружевные перчатки до локтя
- **Гламурный вечер**: чёрное мини-платье без рукавов + полные оперные перчатки + красная помада
- **Casual elevation**: джинсы + блейзер + хлопковые перчатки длиной matinee — лук Bottega Veneta 2026

## Как ухаживать за винтажными перчатками

### Кружевные и тканевые

По совету Naomi Thompson из книги *«Style Me Vintage»*:
1. Замочите перчатки в холодной воде с **содой пищевой** (sodium bicarbonate) на 30 минут
2. Поместите в **наволочку**, чтобы избежать растяжения
3. Стирайте в машине **при 40°C на деликатном режиме**
4. Никогда не смешивайте цвета
5. На жирные пятна — нанесите средство для посуды перед стиркой
6. Сушите **плоско**, не на батарее

### Лайковые (kid leather)

По методу Liz Tregenza:
