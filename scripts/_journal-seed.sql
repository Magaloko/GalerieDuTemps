## Как ухаживать и хранить

1. **Никогда не храните пластик в пластике.** Полиэтиленовые пакеты вызывают «болезнь» — деградацию материала.
2. Используйте **бескислотную папиросную бумагу** и хлопковые мешочки.
3. Храните в **тёмном, прохладном, сухом месте**. Прямой свет вызывает выцветание.
4. Чистите только **мягкой сухой тряпкой**. Влага и моющие средства могут разрушить лаковое покрытие.
5. **Не носите бакелит в бассейн или сауну** — резкие температурные перепады вызывают микротрещины.

