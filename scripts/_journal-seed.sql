## Краткая история перчаток в моде XX века

### 1920-е: смелость и эксперимент

После Первой мировой женщины «освобождаются» от строгого эдвардианского этикета. Перчатки — теперь fashion-выбор, а не социальная обязанность. Цвета — яркие, ткани — необычные.

**Иконичный стиль 1920-х — gauntlet glove** (перчатка-краги), длиной чуть выше запястья с раструбом, напоминающая средневековую. Материал — белая или цветная кожа козы (kid leather).

### 1930-1940-е: военный прагматизм

В Великобритании в 1942 году вводят **Utility Scheme** — рационирование тканей. Перчатки становятся короче, проще, монохромнее. Появляется новый материал — **fabric gloves** из хлопка и ситца, дешёвые и доступные.

В США **L-85 ration system** (1942) тоже ограничивает использование кожи. Главный американский бренд десятилетия — **Van Raalte**.

### 1947: возвращение элегантности с New Look от Dior

**Christian Dior** в феврале 1947 года представляет **Corolle Line** — пышные юбки, осиная талия, новые шляпки. Перчатки **обязательны** к каждому образу. Утренние — белые с лентами и бантиками, вечерние — длинные шёлковые.

### 1950-е: пик популярности и строгий этикет

Десятилетие максимальной формальности перчаток. Длина диктовалась временем дня и формальностью события:

| Длина | Назначение |
|-------|------------|
| **Matinee** (до запястья) | Дневное платье, шопинг |
| **Wrist + bracelet length** | Бизнес-ланч, послеобеденный визит |
| **Elbow length** | Коктейль-вечеринка |
| **Above elbow** | Формальный ужин |
| **Opera (длина до плеча)** | Бал, опера, формальное вечернее платье без рукавов |

В 1953 году **Dents производит перчатки для коронации Елизаветы II** — высшая профессиональная честь для британского ремесленника. Эти перчатки сейчас находятся в коллекции Royal Collection Trust.

### 1960-е: упрощение

Mary Quant и mod-стиль. Перчатки — теперь короткие, кожаные, в ярких цветах. Знаменитые «driving gloves» 1960-х из мягкой телячьей кожи — must-have автомобилистки.

### 1970-1980: затишье и возвращение через рок и поп

В 1970-х перчатки практически не носят — кроме пожилых дам и церковных прихожанок.

