## Как отличить настоящую Birkin/Kelly от подделки

### 1. Запах кожи

Настоящая Hermès пахнет премиальной кожей — глубоко, маслянисто, без химии. Подделка пахнет клеем или пластиком.

### 2. Стежки

Hermès делает сумки вручную одним мастером. Стежки имеют характерный угол наклона ~45°, идеально одинаковые. На подделке стежки часто кривые или машинные.

### 3. Тиснение и шильдики

Логотип **HERMÈS PARIS MADE IN FRANCE** должен быть идеально центрирован, с чётким акцентом на «È». Внутри сумки — кодовая буква в круге или квадрате (производственный год).

### 4. Замок и ключи

Cadenas (замочек) — массивный, тяжёлый. На нём номер, который должен совпадать с номером на ключе. У подделок номера часто отсутствуют или не совпадают.

### 5. Sangles и ремешки

Кожа толстая, края обработаны воском или огнём. У подделки — клееные или прошитые края.

