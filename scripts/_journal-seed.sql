## 1950-е: парюры, кокотейль-кольца и Aurora Borealis (1955)

Послевоенный бум. Christian Dior диктует моду — длинные платья, пышные юбки. Бижутерия снова в моде, и Dior **начинает производить свои собственные ювелирные линии** через лицензированные мастерские (Henkel & Grosse, Kramer, Mitchell Maer) с 1948 года.

**Главное технологическое событие десятилетия — 1955 год: Swarovski изобретает камень Aurora Borealis** — переливающийся радужный страз с покрытием. С этого момента ни один сверкающий парюр 1950-1960-х не обходится без него.

**Парюр** (от старофранцузского *parure* — «украшать», XVII век) — это набор из как минимум трёх согласованных украшений:
- **Full parure** — 4+ предмета: колье, серьги, браслет, брошь (+ иногда тиара)
- **Demi-parure** — 2 предмета (обычно серьги + браслет, или колье + брошь)
- **Married parure** — собранный позже комплект из похожих, но не одного производителя предметов

**Кокотейль-кольца** становятся главным аксессуаром вечера. По этикету 1950-х их носили на безымянном или среднем пальце, обязательно с длинной перчаткой, несмотря на формальные правила, что «кольца не носят поверх перчаток».

**Что искать (1950-е):**
- Подписанные парюры Trifari, Coro Jewelcraft, Weiss — $300-1500
- Кольца-коктейль с пастой и Aurora Borealis — $80-300
- Серьги-клипсы Dior (с маркировкой Henkel & Grosse) — $200-600
- Cocktail-ring Schiaparelli — $500-2000
- Panetta — итальянский ювелир, чьи костюмные пьесы похожи на драгоценные ($150-450)

## 1960-е: flower power, пластик и Mod-эстетика

Mary Quant в Лондоне, Pierre Cardin и Paco Rabanne в Париже — десятилетие перевернуло всё. Бижутерия стала **массовой, цветной, пластиковой**.

**Главные материалы:**
- **Lucite** (акрил) — браслеты-клампсы, серьги-кубики
- **Перспекс** — прозрачные геометрические формы
- **Эмаль** — flower-power-цветы яркой палитры
- **Лак** — в чёрном/белом стиле Op Art

**Дизайнер десятилетия — Kenneth Jay Lane**, начавший карьеру в 1962 году. Его «фальшивые» камни и металлы продавались в Bonwit Teller за десятую часть цены настоящих, но дизайн был такой же сложный. Его носили Жаклин Кеннеди и Барбра Стрейзанд.

**Lea Stein** — парижская дизайнер пластиковых брошек с V-образной застёжкой. Её знаменитая лиса (the fox) сегодня — must-have любого коллекционера. Стоит $80-250.

**Что искать (1960-е):**
- Kenneth Jay Lane (KJL) — серьги, ожерелья — $150-600
- Lea Stein фигурные броши (лисы, кошки, профили) — $80-300
- Trifari Mod-серии — $80-250
- Эмалевые цветы (Crown Trifari, Sarah Coventry) — $50-150

## 1970-е: Navajo-эстетика, камеи и итальянский модернизм

Хиппи, бохо и народные мотивы. Бижутерия 1970-х — это серебро, бирюза, обожжённое дерево и косточки.

**Главный тренд десятилетия — Navajo-инспирированные украшения:** бирюзовые кабошоны в массивных серебряных оправах, бусины, перья, кожа. Сегодня **этот стиль снова на пике моды** — Pinterest и TikTok заполнены образами с тяжёлыми серебряными кольцами и многослойными ожерельями.

**Параллельно — итальянский модернизм:**
- **Elsa Peretti** для Tiffany & Co. (с 1974) — органические серебряные формы (bean, open heart, bone cuff)
- **Lea Stein** продолжает Art Deco-стиль в пластике
- **Pierre Bex** возрождает геометрию деко в массивных эмалевых брошах

**Камеи 1970-х** — последний пик популярности этого античного жанра. Чаще всего — из пластика с рельефом дамской фигуры с пышной причёской. Цена $40-180.

**Что искать (1970-е):**
- Navajo-серебро с бирюзой (особенно работы Zuni, Hopi мастеров) — $100-800
- Elsa Peretti для Tiffany (early period) — $300-1500
- Пластиковые камеи с рельефом — $30-150
