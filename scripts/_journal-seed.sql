## Как проверить подлинность и состояние

### Маркировки
Перевернуть пьесу. Подписи обычно — на застёжке, обратной стороне броши, внутри кольца. Trifari может быть подписан как «TKF», «Trifari», «Crown Trifari» — в зависимости от десятилетия.

### Состояние
- **Целостность пасты/камней.** Один отсутствующий камень снижает стоимость на 30-50%.
- **Покрытие.** Позолота на латуни должна быть равномерной без зелёных пятен (окисление).
- **Застёжка.** Working condition — must. Сломанная безопасная булавка снижает цену вдвое.
- **Реставрация.** Если её делали, цена ниже. Лучше покупать оригинальное состояние.

### Подделки
Современные репродукции 1990-2010-х часто подписаны брендами, а на самом деле сделаны в Китае. Признаки:
- Слишком яркое покрытие
- Лёгкий вес
- Современные крепления (плоские пины вместо изогнутых)

